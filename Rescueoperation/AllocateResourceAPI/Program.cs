
using AllocateResourceAPI.Data;
using AllocateResourceAPI.Repository;
using Microsoft.EntityFrameworkCore;

namespace AllocateResourceAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            builder.Services.AddDbContext<AllocateResourceContext>(options => options.UseSqlServer
            (builder.Configuration.GetConnectionString("AllocateDB")));
            builder.Services.AddScoped<IAllocateResourceRepository, AllocateResourceRepository>();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
